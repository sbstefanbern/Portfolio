          <p class="process-chapter">Idéudvikling</p>
          <p class="process-text">
            Et videoprojekt hvor vi selv skulle filme og redigere. Det eneste krav til videon var at den skulle være under temaet 'sort'. Det var et gruppeprojeket så vi satte os alle ned og begyndte at brainstorme på en storyline og skrive alle ideer ned. Vi fandt her ud af at vi ville have et 'good guy, bad guy' scenarie i vores film og kreere et mørkt scenarie ud fra det hvor der sker noget dårligt for den gode og ikke den onde. Det kombineret med at filme i mørke mente vi ville give os en fed effekt og stemning i videoen iforholdt til temaet. Herefter blev vi mere specifikke med at det skulle omhandle rygning og det skulle være den gode i videoen som ikke røg, men alligevel kollapser, efter at være blevet jagtet af 'den onde' som så slutter af med at ryge i videoen.
          </p>
          <p class="process-chapter">Proces - Videooptagelse</p>
          <p class="process-text">
            Efter vi havde vores idé på plads besluttede vi sted og tidspunkt for at filme. Det skulle være mørkt og et forholdsvist stille sted for at give mere til stemningen i videoen. Vi havde de scener klar som vi ville have skudt. Vi arbejde både med 3.personsperspektiv men gør også brug af 1.personsperspektiv i videoen for at give den mere dynamik og spænding. Vi havde forinden også aftalt at vi skulle have mørk påklædning på for igen at give den en mere dyster og mørk effekt.
          </p>
          <p class="process-chapter">Proces - Redigering</p>
          <p class="process-text">
            Da videoen var skudt kunne vi godt se at vi skulle lysne klippene lidt for at man kunne se hvad der skete. Dette var dog ikke det store problem at gøre i Premiere Pro. Herefter skulle videoen klippes sammen og vi skulle vælge præcis hvilke klip der skulle bruges. Det var også vigtigt for os at finde et stykke dystert musim at ligge ind over. Derudover fandt vi en masse shock lydeffekter at ligge ind over når dramatiske ting i videoen skete. En ekstra detalje vi også lavede var øjenlågseffekten, i et af 1.personsklippene som simulere at man er inde i persons hoved og personen blinker langsomt.
          </p>
          <p class="process-chapter">Kritisk reflektion</p>
          <p class="process-text">
            Til næste gang ville det være en god idé at tænke endnu mere over specielt specifikke klip vi skal bruge og notere dem ned. Vi havde lavet en historie for filmen inden vi begyndte at filme, men vi var ikke særlig specifikke med typen af videoklip vi skulle bruge eller på hvilken måde de skulle filmes bl.a. ift. kameravinkler, perspektiver og længde på klipene. Alt dette ville have været en fordel at have tænkt mere præcist over inden man begynder at lave optagelserne. Derudover blev jeg klar over hvor vigtgit det er med en ordentlig belysning i optagelserne. Vi ville gerne have et mørkt tema men man skal jo stadig kunne se hvad der sker i videoen og det er ikke 100% optimalt at lysne klipene i post-produktion.
          </p>
