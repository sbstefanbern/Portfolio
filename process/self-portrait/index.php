            Jeg tog udgangspunkt i et billede af mig selv. Det lagde jeg så på et låst lag i illustrator og tegnede med pentoolet ovenpå på et nyt lag. Jeg tegnede i processen med en halvtransparent farve så jeg stadig altid kunne se billedet under. For at lave de detaljerede hår fandt jeg en brush som jeg brugte forsigtgit langs håret. Herefter gik jeg ind og fjernede nogle af hårerne og satte nogle nye ind manuelt for at give et mere naturtro look. Skægget er lavet af en lille trekantet brush som jeg selv lavede i Illustrator. Derudover er der anvendt gradienter på min hals samt en meget lille drop shadow på kragen på min skjorte.
