            Jeg gik igang med at finde på nogle ord som jeg ville bringe til livs ved hjælp af typografi. Jeg ville frembringe ordets betydning så man kan føle det i helheden uden at man rent fsaktisk læser selve ordet. Ved 'Murder' er meningen at det er selve ordet som lige er blevet hakket midt over og dermed myrdet. Det skal blodpletterne i baggrunden vise samt det faktum at den øverste del af ordet er 'i live' og r'd mens den nederste del er blevet hakket af og er mere sort og døende. Love tænkte jeg at jeg ville have en forbindelse mellem alle bogstaver for at symbolisere kærlighed og sammenhold. Samtidig med det er o'et gjort til et kærlighedens symbol, et hjerte, som er i samme stil som resten af bogstaverne og stadig er forbundet til dem. Quiet er gjort meget hård og fast i sin udformning for at symbolisere vigtigheden i budskabet. Derudover er stregen i Q'et lavet om til en finger der tyser på en mund for at forstærke det billedlige budskab også. I 'weather' har jeg gået efter en vinterstemning med vind symboler som kører henover toppen af ordet og den kolde blå farve som dominere baggrunden.
