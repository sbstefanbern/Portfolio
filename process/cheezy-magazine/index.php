          <p>
            Mine første tanker gik på at lave en magasinforside om mad. Men det var meget uspecifikt og jeg ville gerne en forside om noget meget specifikt og niche, så man ville kunne køre 100% på et tema. Derfor falgt valget på at lave en forside om Ost, da jeg allerede havde en masse ideer til hvordan det kunne se ud.
          </p>
          <p class="process-chapter">Proces</p>
          <p>
            Jeg startede med at finde det billede som min forside skulle tage udgangpunkt i. Målet var naturligvis at finde et billede hvor gul var den dominerende farve og hvor man fik sat nogle oste i fokus. Herefter begyndte jeg at tegne ost der dryppede ned fra toppen af siden for at forstærke effekten. Herefter fandt jeg en hullede font som igen ligner små oste i hver enkelt bogstav. Jeg fortsatte stilen fra toppen af siden og tegnede også ost der dryppede ned fra fonten. Herefter galdt det om at finde en let font til resten af teksten på forsiden med bløde kanter, ligesom en ost har, som ville passe ind i stilen.
          </p>
          <p class="process-chapter">Kritisk reflektion</p>
          <p>
            Til en anden gang, og hvis jeg også havde haft mere tid, ville jeg gerne have tegnet min egen font til 'CHEEZY' teksten, så jeg kunne blive helt tilfreds med den. Den er lige nu lidt for svær at læse og jeg ville generelt have udformet den anderledes.
