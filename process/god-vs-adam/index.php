          <p class="process-text">
            Denne animation er lavet til en moving art workshop som omhandlede at tage et maleri og bringe det til livs. Her valgte jeg at bruge 'Adams Skabelse' malet af Michelangelo i 1511 som det maleri jeg ville tage udgangspunkt i. Her startede idefasen så med hvad der skulle animeres i billedet. Jeg ville prøve at atge et humoristisk drej på et ellers seriøst maleri, og hvade allerede en masse ideer til sjove ting der kunne ske i maleriet hvis karaktererne bevægede sig.
          </p>
          <p class="process-chapter">Proces - Videooptagelse</p>
          <p class="process-text">
            Jeg startede i Photoshop med at firtskære de to figurere i maleriet som jeg vidste jeg ville animere. Derefter var der noget baggrund der skulle optegnes bag figurerene. Da dette var gjort esporterede jeg seperat, baggrund, gud og adam over i Animate hvorefter jeg så startede selve animationen. Til Animationen har jeg brugt keyframes med Classic Tween, og haft stor fokus på virkelig at bringe de 2 figuerer i live ved også at sætte en masse lyd og stemmer ind som reaktion på forskellige ting der sker i animationen.
          </p>
          <p class="process-chapter">Kritisk reflektion</p>
          <p class="process-text">
            Når jeg ser tilbage på dette projekt kunne det have vælret en god ide for mig at have fuldstendig styr på det jeg ville animere før jeg gik igang. Jeg syntes slutresulytate blev rigtig godt og sjovt, men det er også meget impulsivt og hvad jeg lige følte for at lave og hvad jeg lige fik af gode ideer undervejs som det endte med at blive, frem for et fuldt ud planlagt storyboard.
