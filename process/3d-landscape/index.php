          <p class="process-chapter">Proces</p>
          <p class="process-text">
            Dette landskap er lavet efter frie tøjler i Cinema 4D. Jeg begav mig her mere ud på egen hånd. Idden var at jeg ville skabe et skovmiljø som en man i 1. persons perspektiv vi et kamera skulle bevæge sig igennem. Jeg startede med i stedet for et standard flat 'gulv' at bruge landscape værktøjet til at lave en mere livlig skovbund som ikke var perfekt flad. Herefter begyndte jeg at lave et træ med 'Cylinder' værktøjet, og toppen af træet med 'Cone' værktøjet. Jeg kopierede sp træet for at kreere en skov. Herefter ændrede jeg på træerne størrelse for at skabe variation, samtidig med at jeg lavede små grene rundt omkring på træernes stamme. Efter dette lagde jeg farve på elementerne og satte til sidst kameraet op og animerede det til at gå igennem skoven.
          </p>
          <p class="process-chapter">Kritisk reflektion</p>
          <p class="process-text">
            Jeg skulle istedet for at bruge standard figurer som cylindere og kegler til at lave mine træer have prøvet at bruge mere udfordrernde metoder som jeg både ville have lært mere i programmet af, og som også ville have givet mig nogle pænere træer i sidste ende.
