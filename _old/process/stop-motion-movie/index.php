          <p class="process-chapter">Proces</p>
          <p>
            Dette var en Stop Motion workshop projekt lavede i grupper. Man trak 2 tilfældige sætninger som ens stop motion film skulle omhandle. I vores gruppe trak vi: 'Jeg bryder mig ikke om kuglepenne med rødt blæk' og 'Tage ansvar'. Herfra begyndte idéudviklingen om at inkorporere disse to sætninger i en samlet film. Vi kom frem til en historie om en rød kuglepen som blev mobbet i et skolemiljø og endte emd at begå selvmord fordi ingen af de andre kuglepenne tog noget ansvar. Vi begyndte at tegne vores baggrundsscener på papir og brugte modellervoks for at give liv til nogle kuglepenne, og give dem arme, et hoved og nogle øjne. Herefter tog vi en masse billeder og rykkede en smule på kuglepenene hver gang for at simulere animation. Alle billederne bliver til sidst samlet og lavet til en film.
          </p>
          <p class="process-chapter">Kritisk reflektion</p>
          <p>
            Det kunne være fedt at gøre noget mere ud af sceneriet i baggrunden i vores film. Den bliver en smule kedelig at se på fordi den mangler noget blikfang. Derudover ville jeg personligt gerne pgså have brugt modellervoksen lidt mere til animation. Vi bruger det eksempelvis i filmen når en tårer triller ned af den røde kuglepens kind. Men jeg ville til næste gang lave endnu mere af dens slags stop motion.
