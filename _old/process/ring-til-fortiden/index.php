          <p class="process-chapter">Idéen</p>
          <p>
            Med vores ide kan du på Fredericia Vold komme ud at opleve gamle soldater og krigshelte fra de gamle krige, fortælle deres historie og give dig spor til selv at finde hvor de er på volden. Når du så har fundet dem, vil du kunne se dem via Augmented Reality (AR). Her vil du så igen kunne snakke med dem hvor de denne gang vil stille dig et spørgsmål om dem selv som, hvis du har hørt efter deres historie, vil kunne svare på for at kunne få mulighed for at få kontakt til en ny soldat. Når du løbende får snakket med flere og flere soldater vil du, via appen, kunne tage et billede af dig selv hvor alle de soldater du har fundet vil stå i baggrunden og være med dig på billedet. Dette billede vil du så kunne tage med hen på Fredericia Bymuseum og få printet i et fint diplomdesign, som du kan vise overfor dine venner og familie.
          </p>
