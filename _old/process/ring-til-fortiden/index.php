          <p class="process-chapter">Kritisk reflektion</p>
          <p>
            I forbindelse med mit arbejde på Ring til Fortiden har jeg fået rigtig god forståelse for vigtigheden af planlæggelse. Dette projekt blev meget presset for mig og min gruppe de sidste par dage fordi vi havde sat så høje ambitioner som vi gerne ville nå, men havde ikke arbejdet nok i starten af projektet så meget af arbejdet blev fordelt ud over de sidste par dage af projektet før fremvisningen. Det at have en fornemmelse for hvor lang tid de ting man har sat sig for faktisk tager at lave er en super vigtig ting, og noget der er vigtigt at tage i mente når man planlægger. Ting tager også ofte længere end man regner med, specielt når man er perfektionistisk og har et højt ambitionsniveau. Specielt i produktionen og redigeringen af promoveringsvideoen. I forbindelse med videoen har jeg også lært hvor vigtigt det er at lave et storyboard forinden man begynder med skydning, og redigering af video. Vi brugte lidt for lang tid på at køre frem og tilbage i redigeringsfasne mellem forskellige ideer, fordi vi ikke havde lagt os fast på én måde at gøre det på forinden vi begyndte på at filme.
          </p>
