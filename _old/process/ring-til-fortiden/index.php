          <p class="process-chapter">Idéudvikling</p>
          <p>
            Opgaven lød på at producere en High-Fidelity mockup af en idé til en installation på Frederica Vold som skulle have til formål at bringe flere gæster og mere opmærksomhed omkring den. Dette var en gruppeprojekt hvor vi kom frem til vores ide udfra nogle problemformuleringer som vi gerne ville have løst. Ideen blev udviklet og tilpasset udfra dataindsamling, personas, brainstorming samt en SWOT-analyse. Det vi gerne ville opnå med vores ide var at skabe et aktivt miljø på Fredericia Vold. Vores hovedmålgruppe var børn og unge. Vi ville gerne have folk aktivt ud at røre sig på volden samtidig med at vi ville skabe opmærksomhed på voldens historiske rolle for Fredericia. Vi ville gerne have man lærte noget af vores ide, uden at man bare læste, men istedet lære ved at opleve og udforske.
